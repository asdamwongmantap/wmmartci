function cekphonecode1(checkbox){
	 if(checkbox.checked == true){
       $("#PHN_AREA_1NEW").show();
	   $("#PHN_AREA_1DISABLE").hide();
	   $("#PHN_AREA_1DISABLE").val('PHN_AREA_1');
    }else{
        $("#PHN_AREA_1NEW").hide();
		$("#PHN_AREA_1NEW").val('');
		$("#PHN_AREA_1DISABLE").show();
		$("#PHN_AREA_1DISABLE").val('');
   }
}
