function cekmailaddr(checkbox){
	 if(checkbox.checked == true){
       $("#MAILING_ADDRNEW").show();
	   $("#MAILING_ADDRDISABLE").hide();
	    $("#MAILING_ADDRDISABLE").val('MAILING_ADDR');
    }else{
        $("#MAILING_ADDRNEW").hide();
		$("#MAILING_ADDRNEW").val('');
		$("#MAILING_ADDRDISABLE").show();
		 $("#MAILING_ADDRDISABLE").val('');
   }
}
